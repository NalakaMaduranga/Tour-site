                    <h3>Madu River</h3>
                    <p class="day-tour_para">In the Southern region of Sri Lanka lies the Galle district. Balpitiya is a little town in the district and would be quite unimportant; except for the location of a river. The Madu River is very rich in biodiversity. It passes through the wet zone of Sri Lanka opening up into the large Madu Lagoon on its way to the Indian Ocean. The Madu River area surrounding the river are all swampy marshlands covered in mangrove forests and those forests covers over 61 hectares, that is over 150 acres,14 of the 24 species of mangroves are found in this area. The largest animal in the region is the wild boar. There are other smaller animals such as monkeys, a variety of squirrels and etc. When it comes to birds, cormorants and kingfishers are a common sight. For avid bird watchers, the mangrove forests are a dream coming true. There are over 111 bird species identified to inhabit the region. With most of the area being soggy wetlands, the solid landmass of the region mainly consists of islands. History states there used to be 64 islands along Madu River. However most seem to have sunk under the water, as only 25 islands are reported today. Of these islands only 15 have sizable landmass.Interesting things to be visited of the area is the Open-Air Fish Massage & Cinnamon Island. . The fish massage is quite popular around the world, especially in East Asian countries. The unique factor about this massage is that the fish are held in their natural environment, the river.The main source of economy for the locals of the Madu Ganga (River) region is the cinnamon industry. The freshly cinnamon is brought here to be peeled. By default, this also means that the best quality cinnamon can be purchased at bargain prices here.</p>
