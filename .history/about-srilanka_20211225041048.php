			<h3 class="tittle">About Srilanka</h3>
			<br>
			<h5>Golden beaches, ancient temples and laid-back safari</h5>
			<br>
            
			<p>The island nation of Sri Lanka might be small, but beyond its Indian Ocean-lapped shores you’ll find a country of startling beauty and genuine warmth.
Cinnamon and spices brought traders to Ceylon from around the world – first the Portuguese, followed by the Dutch, who fortified the ports of Colombo and Galle, before the British arrived for the tea. This history has lent Sri Lanka a diverse culture, reflected in the country’s cuisine and architecture.
It is home to hundreds of miles of palm-lined beaches, rolling hills of verdant tea plantations punctuated with nimble-fingered pickers, millennia-old temples sat alongside faded colonial mansions, and ancient forests where herds of elephants roam.
Hindu and Buddhist kingdoms date back thousands of years, gloriously preserved at UNESCO World Heritage Sites such as Kandy, Anuradhapura and Polonnaruwa, the area between which is known as the Cultural Triangle.
It is an island with swathes of wilderness and some fine safari, whether it’s leopard spotting in Yala National Park, or encountering herds of elephant, surviving here in a higher density than anywhere else in Asia, in  parks such as Udawalawe. Birders will be in their element with over 200 different types of birds to spot, while whales and dolphins populate the island’s warm coastal waters, and five of the world’s seven turtle species come to nest in Sri Lankan sand.</P>
<br>
<h3>When to go</h3>
<br>
<p>Sri Lanka’s tropical climate generally offers warmth and sunshine throughout the year. The east coast is best visited between March and September, whilst the south coast is at its peak between November and March. Sri Lanka is affected by two monsoon seasons caused by the winds originating from the Indian Ocean and the Bay of Bengal. The sout west monsoon (Yala) brings rain to Colombo and the south and west coasts during May and June. The north east monsoon (Maha) affects the far north and east between December and February.</p>
<br> <h2>kandy</h2><br>

<p>A world of ancient kings, ornate temples, lush gardens and vibrant traditions – Kandy is the second largest city in Sri Lanka and its cultural capital.</p>
<p>Nestled in the midst of hills and surrounded by tropical paddy fields and tea plantations, the town is the home of the Temple of the Tooth Relic (Sri Dalada Maligawa), the country’s most important religious shrine.</p>
<p>At the heart of the city is the man-made Kandy Lake, where a scenic shaded path offers views of the surrounding hills and town, ideal for walkers, joggers and bird spotters. Browse bustling market places with bright, fresh produce, local delicacies and fragrant spices, or spend the day exploring the opulent temples and learning about the city’s rich history. The dense downtown area is surrounded by tropical rainforest, so look out for exotic monkeys swinging overhead.</P>
<p>Kandy served as the capital of the last Sinhalese kingdom, before falling to the British in 1815 and navigating its streets reveals an intriguing mix of old Kandyan and colonial buildings. With the vibrant green of the highlands as a backdrop, Kandy is not only Sri Lanka‘s most cultural city, but one of its most beautiful.</p>

<br>
<h3>What to do</h3><br>
<ul>
<li>Visit the Temple of the Tooth Relic, the country's most important religious shrine</li>
<li>Hike through the surroundings hills</li>
</ul>
<h3>When to go</h3><br>
<p>The best time to visit Kandy is between December and April. The wet season falls between May and August, with October being another wet month. Average temperatures in Kandy are 20-22°C.</p>




<br><h2>Galle</h2><br>
<p>Galle is a stunningly atmospheric town at the southern edge of Sri Lanka with fascinating history evident at every turn through its eclectic assemblage of architecture. .</p>
<p>Galle Fort itself is sat on an angular promontory that juts out into the sea. An important seaport for the spice trade long before the arrival of Europeans, the Portuguese arrived in the 16th century only to be usurped by the Dutch who built the impressive granite fort – the largest of its kind in Asia – that encloses and demarcates the old town, before they in turn were replaced by the British. .</p>
<p>Today the walls and everything inside them are a UNESCO World Heritage Site, with beautiful Dutch fusion architecture, a commanding Victorian lighthouse, grand churches and mosques, all in a sultry tropical setting. .</p>
<p>But the town also refuses to become a relic, with numerous civic buildings still fulfilling their original purpose and the modern town extending inland. Spice traders and merchants have been replaced by an equally cosmopolitan crowd of artists, writers, designers and poets, and its small streets are packed with boutique shops, buzzing little bars and cafes as well as a smattering of charming hotels. The fishermen sat over the waves on their stilts carry on angling as they always have done.</p>


<br>
<h3>What to do</h3><br>
<p>
•	Explore Galle Fort, a UNESCO World Heritage Site<br></p>
<p>
•	Browse its boutiques and vibrant markets<br></p>
<p>
•	Visit the National Maritime Archaeology Museum<br></p>
<p>
•	Learn all about one of the world's most popular drinks at the Handunugoda Tea Museum<br> </p><br>
<h3>When to go</h3><br>
<p>The south coast is at its peak between November and March - the seas are considered unsafe for swimming outside of these respective months. The monsoons bring rain in May and June, with an inter-monsoon affecting October and early November. Average temperatures are 27-30° but a sea breeze can make it feel a little cooler</p>



<br><h2>Cultural Triangle</h2><br>

<p>Kandy, Anuradhapura and Polonnaruwa were the three great Sinhalese capitals, and together they form the area known as the Cultural Triangle. .</p>
<p>The heart of Sri Lanka‘s ancient civilisation, this historic region is crammed full of temples and monuments and its ruined cities are fascinating places to explore. Anuradhapura was the county’s capital for over 1,000 years and was one of the greatest monastic cities of its time, with thousands of monasteries dotted across the land. .</p>
<p>Polonnaruwa also served as the capital, but for a much more modest 200 years. It offers a compelling insight into medieval Sri Lanka and what was one of the great urban centres of South Asia. .</p>
<p>One of Sri Lanka‘s most visited and awe inspiring sites is the palace and fortress complex of Sigiriya. Perched atop a huge rocky plateau, the site initially served as a monastery before King Kasyapa built his royal residence here. The entrance was built in the form of a huge lion, which eared it the name ‘Lion Rock’ but today only his feet remain – though it has to be said, they are some very large and impressive feet. .</p>
<p>Also well worth visiting are the cave temples of Dambulla and Mihintale, the birthplace of Buddhism in Sri Lanka. .</p>
<p>It’s not all ancient ruins, however, and the nearby Minneriya National Park with its herds of elephant, sambar deer, purple-faced langur monkeys, sloths and very elusive leopards is the perfect place to experience Sri Lanka’s wilderness. .</p>

<br><h3>What to do</h3><br>
<p>
•	Visit the ancient cities of Anuradhapura and Polonnaruwa, admiring temples, stupas and statues.</p>
<p>
•	Explore the magnificent hilltop fort and palace complex of Sigiriya.</p>
<p>
•	Marvel at the cave temples of Dambulla, the largest and best-preserved cave-temple complex in Sri Lanka. Buddhist mural paintings cover the walls and there are over 150 statues.</p>
<p>
•	Get your wildlife fix at Minneriya National Park, home to elephants, monkeys, deer and a mind boggling array of birdlife.</p>
<br><h3>When to go</h3><br>
<p>Average temperatures in the Cultural Triangle are between 26-28°C. The most pleasant time to visit is in the dry season between April and September while the wetter months are from October to November. .</p>
<br><h2>Yala </h2><br>

<p>Occupying the south-eastern corner of the island, the vast Yala National Park is Sri Lanka’s second largest national park and also its most popular. .</p>
<p>The park itself is divided into five blocks, two of which, Ruhuna and Kumana, can be explored on guided game drives, and stretches from the sea inland, encompassing some 979 square kilometres (378 square miles) of forest, wetlands and savannah. This varied landscape provides a vital home for hundreds of Sri Lankan elephant, sloth bear, crocodile and rare wild water buffalo, as well as over 200 bird species. .</p>
<p>But it is the leopard, found here in perhaps the highest concentration in the world, that are iconic of the park. Their secretive nature means spotting Sri Lanka’s top predator is still a challenge, but accompanied by a skilled guide and a little luck, there a few better places in the world to find these enigmatic cats. .</p>
<br><h3>When to Go</h3><br>
<p>Yala National Park can be visited year round, depending on your interests. The rainy season, from November to April sees short dramatic downpours and brings with it lush vegetation and wildflowers. This is the best time for birders, with many migratory species flying from as far north as Siberia to escape the winter and is good a good time for spotting elephants. The dry season from May to October sees the bush die back and animals congregating around water holes making it the easiest time of year to see leopards, elephants, buffalo, spotted deer, wild boar, eagles, owls and kingfishers though it is hot and dusty. .</p>











<br><h2>Colombo</h2><br>

<p>On the western coast of the island, Colombo is a sprawling city home to over five million people. It is traditionally centred around Fort, a business district with its roots as a fortified Portuguese trading post, and the green waters of Beira Lake, all facing out to sea. .</p>
<p>Hot and busy, recent years have seen Colombo gradually transform from a place visitors could not avoid to a fascinating destination well worth exploring. Its historic quarters and grand 19th-century architecture have been restored, the seafront spruced up and cosmopolitan vibe revived with numerous museums, galleries and characterful eateries opening that celebrate its unique and diverse heritage. .</p>
<p>The city is dotted with temples, shrines, cafes, markets and is best explored by tuk tuk and there are some idyllic beaches to be discovered a little way along the coast. .</p>

<br><h3>When to go</h3><br>
<p>Temperatures average between 27-30 °C in Colombo, peaking in April. The driest months are between December and March and the wettest between May and June, and then again in October and early November. .</p>


<br><h3>What to do</h3><br>
<p>
•	Discover the city's Buddhist shrines and Hindu temples.</p>
<p>
•	Explore the National Museum.</p>
<p>
•	Shop for souvenirs in the city's colourful bazaars.</p>








<br><h2>Tea Country</h2><br>

<p>A train winds its way slowly through undulating hills blanketed in green. Dotted across lush ranked fields, women quickly but meticulously harvest leaves by hand, baskets slung across their backs. Stretching across the island’s midsection, this is Sri Lanka’s tea country and quite possibly the origin of your favourite morning cuppa. .</p>
<p>Sri Lanka (formerly known as Ceylon) is the world’s fourth largest producer of tea. It is also one of the few places in the world where harvesting is still done by hand. Skillful workers deftly pluck the most flavoursome and aromatic parts of the bushes – the topmost leaves and buds – which are then sent to nearby factories to be dried and processed. .</p>
<p>The first tea plantations were established in Kandy in the 1860s and now most of the scenic central region’s elevated slopes are covered in contoured rows of painstakingly maintained .</p><p>plants. Breathe deeply as you stroll through the lush plantations, and take time to learn about the leaves’ historic journey from soil to cup at the Ceylon Tea Museum in Hantana, Kandy. There you can sample some of the premium teas produced in Sri Lanka and learn about the fine art of tea appreciation while enjoying views over the museum’s grounds which are landscaped with the different tea varietals. .</p>
<p>Many of the region’s tea factories offer tours which include walks through the plantations, a closer look at the manufacturing process and tastings. If you’ve never understood the joy a simple cup of tea can bring, this may be the perfect place to start. .</p>

<br><h3>What to do</h3><br>
<p>
•	Wind your way through the lush tea plantations.</p>
<p>
•	Visit a local factory to learn all about Sri Lanka's tea industry.</p>
<br><h3>When to Go</h3><br>
<p>The temperature in the hills is significantly cooler than in the cities, averaging between 17-20°C. Humidity is also lower here. The dry seasons, and the best times to visit, are between December and March and then again from July to August. You'll experience wetter weather conditions in April and from October to November. .</p>




<br><h2>Mirissa</h2><br>

<p>It is the jewel of southern Sri Lanka’s coast. From whale watching to surfing, Mirissa offers a lot more than you know. Located between Galle and Tangalle, this laid-back seaside town offers the right balance of excitement and beach solitude. Its beautiful bay makes a stunning crescent beach, and dense coconut palms fringe its edges. Mirissa whale watching is the most anticipated experience as it sits alongside one of the world’s great cetacean migratory routes. It is the best place to see blue whales and sperm whales in Sri Lanka. Spinner dolphins too swim in its waters. Surfing conditions here are ideal, and it attracts pro-surfers and beginners alike. Mirissa nightlife is just as exhilarating! .</p>

<br><h3>What to do</h3><br>
<p>
•	Whale Watching.</p>
<p>
