<br><h3>When to go</h3><br>
<p>The south coast is at its peak between November and March - the seas are considered unsafe for swimming outside of these respective months. The monsoons bring rain in May and June, with an inter-monsoon affecting October and early November. Average temperatures are around 27°C but a sea breeze can make it feel a little cooler. .</p>








<br><h2>Ella</h2><br>
<p>In the misty highland of Sri Lanka is Ella, a lush green village with jaw-dropping views all around. Its landscape is dotted with mountains, valleys, and features Sri Lanka’s most famous railway line. The waterfalls of Ella are the most beautiful. On a clear night, the distant glow of the Great Basses lighthouse on the south coast is visible from Ella. And, at times, you can even see the Uda Walawe National Park. Situated in the tea country, with a 1000m elevation, its tea plantations in Ella thrive all around. Fresh mountain air and the mild climate make Ella a quiet scenic retreat for any weary raveler. There are plenty of mountain treks for a lovely walking holiday. .</p>

