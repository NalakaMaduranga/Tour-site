			<h3 class="tittle">About Srilanka</h3>
			<br>
			<h5>Golden beaches, ancient temples and laid-back safari</h5>
			<br>
            
			<p>The island nation of Sri Lanka might be small, but beyond its Indian Ocean-lapped shores you’ll find a country of startling beauty and genuine warmth.
Cinnamon and spices brought traders to Ceylon from around the world – first the Portuguese, followed by the Dutch, who fortified the ports of Colombo and Galle, before the British arrived for the tea. This history has lent Sri Lanka a diverse culture, reflected in the country’s cuisine and architecture.
It is home to hundreds of miles of palm-lined beaches, rolling hills of verdant tea plantations punctuated with nimble-fingered pickers, millennia-old temples sat alongside faded colonial mansions, and ancient forests where herds of elephants roam.
Hindu and Buddhist kingdoms date back thousands of years, gloriously preserved at UNESCO World Heritage Sites such as Kandy, Anuradhapura and Polonnaruwa, the area between which is known as the Cultural Triangle.
It is an island with swathes of wilderness and some fine safari, whether it’s leopard spotting in Yala National Park, or encountering herds of elephant, surviving here in a higher density than anywhere else in Asia, in  parks such as Udawalawe. Birders will be in their element with over 200 different types of birds to spot, while whales and dolphins populate the island’s warm coastal waters, and five of the world’s seven turtle species come to nest in Sri Lankan sand.</P>
<br>
<h3>When to go</h3>
<br>
<p>Sri Lanka’s tropical climate generally offers warmth and sunshine throughout the year. The east coast is best visited between March and September, whilst the south coast is at its peak between November and March. Sri Lanka is affected by two monsoon seasons caused by the winds originating from the Indian Ocean and the Bay of Bengal. The sout west monsoon (Yala) brings rain to Colombo and the south and west coasts during May and June. The north east monsoon (Maha) affects the far north and east between December and February.</p>

